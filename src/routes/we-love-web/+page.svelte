    <h2>Lezing Niels Leenheer</h2>
    <p>
        De lezing van Niels Leenheer ging over verschillende aspecten van webontwikkeling en de veerkracht van HTML.
        Een belangrijk punt was dat, hoewel je in een Node script code kunt toevoegen, HTML tolerant is voor fouten.
        Iedereen kan een website maken en de broncode ervan bekijken, en een webmaster, zoals Tim, is in feite een
        webdeveloper. Soms zoeken mensen onnodig ingewikkelde oplossingen voor simpele problemen, zoals Tim, die een
        browser ontwikkelde die tegelijkertijd een editor was, waarmee hij al vroeg het belang van fouttolerantie
        aantoonde.
    </p>
    <p>
        Browsers negeren HTML-elementen die ze niet herkennen zonder foutmeldingen te geven. Als een browser
        scripting niet ondersteunt, wordt gewoon tekst weergegeven; anders wordt de inhoud verborgen. Frameworks
        bieden backwards compatibility, maar dit stopt op een gegeven moment. Polyfills kunnen gebruikt worden voor
        onbekende tags. Een voorbeeld hiervan is de `blink` tag, die in de jaren 90 populair was. Nieuwe elementen
        mogen geen invloed hebben op bestaande elementen. Met CSS kan je een `blink` tag stylen.
    </p>
    <p>
        Tussen HTML en de DOM wordt een tokenizer en tree builder gebruikt om de code weer te geven. De treebuilder
        maakt de DOM vanuit de HTML-code door elk teken te analyseren, inclusief tag namen, attribuut namen en
        waarden, en zet deze om in tokens. De treebuilder stopt niet bij parse errors, zolang de eerste letter een
        'less than' symbool is, wordt het nog steeds gelezen en komt er een parse error uit. Dit illustreert hoe
        robuust de browser is. De browser heeft oplossingen voor diverse zaken, zoals Unicode en emoticons. De
        tokenizer geeft tokens aan de treebuilder, wat complexer is dan het lijkt. De tokenizer ziet hier drie
        onderdelen in, waarmee de tree wordt opgebouwd. Als de treebuilder HTML tegenkomt dat niet correct genest is,
        wordt dit ook opgelost. De HTML-validator gaat terug tot HTML 2.0.
    </p>
</section>

