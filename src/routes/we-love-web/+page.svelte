    <h2>Webinar van marie bij uncinc</h2>
    <p>
        Human-centered design is van groot belang, waarbij gekeken wordt naar de leefwereld van de eindgebruiker.
        Dit omvat digitale toegankelijkheid en WCAG (Web Content Accessibility Guidelines). Een voorbeeld van een
        probleem dat gebruikers kunnen ervaren, is het niet kunnen inplannen van een afspraak bij een
        gezondheidsapp, omdat deze niet toegankelijk is gemaakt voor screenreaders.
    </p>

    <p>
        Een beperking kan variëren: iemand kan één arm hebben, blind of doof zijn. Veel mensen hebben een beperking
        zonder dat deze zichtbaar is. WCAG staat voor Web Content Accessibility Guidelines, en een website moet
        hieraan voldoen, waarbij er vier basisprincipes zijn: waarneembaar, bedienbaar, begrijpelijk en robuust.
    </p>

    <p>De EAA (European Accessibility Act) wet komt eraan.</p>

    <p>
        De niveaus van toegankelijkheid zijn A (het minimale), AA (medium, dus wel het minimale en een paar extra
        dingen), en AAA (optimaal toegankelijk, waarbij je website heel toegankelijk is). De wet geldt voor
        bedrijven zoals B2C-bedrijven met meer dan 10 medewerkers, en voor bedrijven die online producten of
        diensten aanbieden.
    </p>
</section>


